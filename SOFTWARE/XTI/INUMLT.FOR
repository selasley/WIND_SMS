      SUBROUTINE INUMLT ( NINT, NASC )
C
C   EXPANDED FROM DCH'S INUMLT.FOR TO INCLUDE LOWER CASE ASCII CHARCTERS
C
C   ACCEPTS THE INTEGER NINT IN THE RANGE 0:71 
C   AND RETURNS AN ASCII CHARACTER AS NASC
C   NINT >=72 --> *
C
C   VERSION: 15 JUNE 1990  <--  17 SEPT 1984
C	
	INTEGER*4 NINT

	BYTE NASC
C
C   SET NINT = 0 TO A SPACE
C
D       DO NINT = 0,65

	IF ( NINT .LE. 0 )    THEN
            NASC='40'O	
C
	ELSE IF ( NINT.GE.1 .AND. NINT.LE.9 )    THEN
            NASC=NINT+'60'O
C
C   SET 10-35 = a:z      36:71 = A-Z   >=72 = *
C
	ELSE               IF (NINT.GE.10 .AND. NINT.LE.35) THEN
            NASC=(NINT+32)+'67'O
	ELSE               IF (NINT.GE.36 .AND. NINT.LE.61) THEN
            NASC=(NINT-26)+'67'O
        ELSE               IF (NINT.GE.62) THEN
            NASC='52'O
        ENDIF
D        WRITE(6,1111) NINT,NASC,NASC
D 1111   FORMAT(I10,I10,10X,A)
D        ENDDO
D        DO NINT = 52,122
D          WRITE(6,1112) NINT,NINT
D 1112   FORMAT(I10,10X,A)
D        ENDDO
C
	RETURN
DC        STOP
	END
