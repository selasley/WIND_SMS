        PROGRAM TESTCOMPRESS
        INTEGER WIND_DECOMPRESS,INPUT,DUMMY,OUTPUT2
        BYTE WIND_COMPRESS,OUTPUT
        LOGICAL CODE_C
C        
 10     TYPE*,' ENTER A VALUE TO COMPRESS:'
        ACCEPT*, INPUT
        TYPE*,' ENTER CODE: 0=A, 1=C:'
        ACCEPT*, DUMMY
        IF(DUMMY.EQ.0)THEN
          CODE_C=.FALSE.
        ELSE
          CODE_C=.TRUE.
        ENDIF
        OUTPUT=WIND_COMPRESS(INPUT,CODE_C)
        TYPE 30,OUTPUT
 30     FORMAT(1X,Z2)
        OUTPUT2=WIND_DECOMPRESS(OUTPUT,CODE_C)
        TYPE 40,OUTPUT2
 40     FORMAT( ' INPUT WAS ',I8)
        GO TO 10
        END
