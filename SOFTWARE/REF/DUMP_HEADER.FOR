      SUBROUTINE DUMP_FILE_HEADER(IOUT,FILE_HEADER)
      IMPLICIT NONE
      INCLUDE 'INC_DIR:L1FMT.INC/NOLIST'
C
      INTEGER IOUT,NCH
      RECORD /ST_L1_FILE_HEADER/ FILE_HEADER
C
      READ(FILE_HEADER.COMMENT_LEN,1001) NCH
1001  FORMAT(I3)
C
      WRITE(IOUT,1002)
     1  FILE_HEADER.IDENT,
     1  FILE_HEADER.HEADER_VERS,
     1  FILE_HEADER.INPUT_FILE,
     1  FILE_HEADER.MEDIUM,
     1  FILE_HEADER.CREDATE_DOY,
     1  FILE_HEADER.CREDATE_YEAR,
     1  FILE_HEADER.CREDATE,
     1  FILE_HEADER.DATE_OF_EXE,
     1  FILE_HEADER.HW_TYPE,
     1  FILE_HEADER.RUN_DATE,
     1  FILE_HEADER.START_TIME,
     1  FILE_HEADER.STOP_TIME,
     1  FILE_HEADER.COMMENT_LEN,
     1  FILE_HEADER.COMMENT(1:MAX(1,NCH)),
     1  FILE_HEADER.L1_RECORDS,
     1  FILE_HEADER.EDB_COUNT,
     1  FILE_HEADER.EDB_LOST,
     1  FILE_HEADER.EDB_SKIP
C
1002  FORMAT(
     1  '  IDENT        = ',A4/
     1  '  HEADER_VERS  = ',A2/
     1  '  INPUT_FILE   = ',A72/
     1  '  MEDIUM       = ',A2/
     1  '  CREDATE_DOY  = ',A3/
     1  '  CREDATE_YEAR = ',A4/
     1  '  CREDATE      = ',A11/
     1  '  DATE_OF_EXE  = ',A23/
     1  '  HW_TYPE      = ',A1/
     1  '  RUN_DATE     = ',A11/
     1  '  START_TIME   = ',A20/
     1  '  STOP_TIME    = ',A20/
     1  '  COMMENT_LEN  = ',A3/
     1  '  COMMENT      = ',A80/
     1  '  L1_RECORDS   = ',A4/
     1  '  EDB_COUNT    = ',A6/
     1  '  EDB_LOST     = ',A6/
     1  '  EDB_SKIP     = ',A6
     1  )
      RETURN
      END
      SUBROUTINE DUMP_SR_HEADER(IOUT,SR_HEADER)
      IMPLICIT NONE
      INCLUDE 'INC_DIR:L1FMT.INC/NOLIST'
C
      INTEGER IOUT
      RECORD /ST_L1_SR_HEADER/ SR_HEADER
C
      WRITE(IOUT,1001)
     1  SR_HEADER.IDENT,
     1  SR_HEADER.NVALID,
     1  SR_HEADER.PRESENT(1),SR_HEADER.PRESENT(2),
     1  SR_HEADER.BITRATE_CODE,
     1  SR_HEADER.SR_COUNT,
     1  SR_HEADER.ZERO_SWICS_PHA(1),SR_HEADER.ZERO_SWICS_PHA(2),
     1  SR_HEADER.ZERO_MASS_PHA(1),SR_HEADER.ZERO_MASS_PHA(2),
     1  SR_HEADER.ZERO_STICS_PHA(1),SR_HEADER.ZERO_STICS_PHA(2)
C
1001  FORMAT(
     1  '   IDENT           = ',A4/
     1  '   NVALID          = ',I10/
     1  '   PRESENT         = ',Z8.8,2X,Z8.8/
     1  '   BITRATE_CODE    = ',I10/
     1  '   SR_COUNT        = ',I10/
     1  '   ZERO_SWICS_PHA  = ',Z8.8,2X,Z8.8/
     1  '   ZERO_MASS_PHA   = ',Z8.8,2X,Z8.8/
     1  '   ZERO_STICS_PHA  = ',Z8.8,2X,Z8.8
     1  )
      RETURN
      END
